package comparableComparator;

import java.util.Comparator;


public class studentScore {
	private int score;
	private String name;
	public studentScore(int score,String name) {
		this.score=score;
		this.name=name;
	}
	public int getScore() {
		return score;
	}
	public void setScore() {
		this.score=score;
	}
	public String getName(String name) {
		return name;
	}
	public void setName() {
		this.name=name;
	}
	
	
	

}
